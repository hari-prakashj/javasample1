package com.onesoft.day4;

public class Vowels {

	public static void main(String[] args) {
		String a = "aeroplane";
		
		if(a.contains("a")||a.contains("e")||a.contains("i")||a.contains("o")||a.contains("u")) {
			System.out.println(a+" is a vowel");
		}
		else {
			System.out.println(a+" is not a vowel");
		}

	}

}
