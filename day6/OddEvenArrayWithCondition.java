package com.onesoft.day6;

public class OddEvenArrayWithCondition {

	public static void main(String[] args) {
		int[] integer = {-10,25,-87,36,54,0,-93,72};
		for(int i=0;i<integer.length;i++) {
			if(integer[i]%2==0) {
				System.out.println(integer[i]+" is even");
			}
			else {
				System.out.println(integer[i]+" is even");
			}
		}

	}

}
