package com.onesoft.day1;

public class IntArithmeticOperators {
	public static void main(String[]args) {
		int num1 = 10;
		int num2 = 20;
		int total1 = num1+num2;
		int total2 = num2-num1;
		int total3 = num1*num2;
		int total4 = num2/num1;
		int total5 = num1%num2;
		System.out.println("The total is =" +total1);
		System.out.println("The total is =" +total2);
		System.out.println("The total is =" +total3);
		System.out.println("The total is =" +total4);
		System.out.println("The total is =" +total5);
	}


}
